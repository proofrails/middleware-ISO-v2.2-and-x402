const baseTimeline = (status: ProofRecord['status']): ProofRecord['timeline'] => {
  const done = status !== 'Pending' && status !== 'Failed';
  const anchored = status === 'Anchored' || status === 'Verified';
  const verified = status === 'Verified';
  return [
    {
      id: 't1',
      label: 'Event ingested',
      detail: 'Source payment normalized to Proof Record schema',
      at: '2026-04-02T14:18:02Z',
      state: 'done',
    },
    {
      id: 't2',
      label: 'Evidence bundle',
      detail: 'SHA-256 manifest generated; ISO artifacts attached',
      at: '2026-04-02T14:18:14Z',
      state: done ? 'done' : 'current',
    },
    {
      id: 't3',
      label: 'Flare anchor',
      detail: 'Commitment posted to Coston2 evidence registry',
      at: anchored ? '2026-04-02T14:19:41Z' : '—',
      state: anchored ? 'done' : done ? 'current' : 'pending',
    },
    {
      id: 't4',
      label: 'Verification',
      detail: 'Cross-check bundle hash against on-chain anchor',
      at: verified ? '2026-04-02T14:22:08Z' : '—',
      state: verified ? 'done' : anchored ? 'current' : 'pending',
    },
  ];
};

